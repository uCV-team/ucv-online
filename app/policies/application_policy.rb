class ApplicationPolicy
  attr_reader :user, :newsletter

  def initialize(user, newsletter)
    @user = user
    @newsletter = newsletter
  end

  def index?
    false
  end

  def show?
    false
  end

  def create?
    false
  end

  def new?
    create?
  end

  def update?
    false
  end

  def edit?
    update?
  end

  def destroy?
    false
  end

  delegate :admin?, to: :user

  def not_admin?
    user.admin? == false
  end

  class Scope
    attr_reader :user, :scope

    def initialize(user, scope)
      @user = user
      @scope = scope
    end

    def resolve
      scope.all
    end
  end
end
